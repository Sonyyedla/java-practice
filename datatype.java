class DataType{
	public static void main (String args[]) {
		// including character
		char a = 'S';
		//including integer
		int i = 2;
		//include float;
		float f = 4.00f;
		//include byte;
	    byte b = 2;
	    //include long integer;
	    long l = 3956;
	    //print all the datatypes and get the output;
	    System.out.println("char:" +a);
	    System.out.println("int:" +i);
	    System.out.println("float:"+f);
	    System.out.println("byte:"+b);
	    System.out.println("long:"+l); 
	}
}